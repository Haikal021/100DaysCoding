public class day16 {
    public static void main(String[] args) {
        int angka = 123;
        String angkaStr = String.valueOf(angka);
        

        double angkaDesimal = 3.14159;
        String angkaDesimalStr = String.valueOf(angkaDesimal);
   

        boolean benar_Salah = true;
        String benar_SalahStr = String.valueOf(benar_Salah);
        

        System.out.println(" tipe data String dalam bentuk String : " + angkaStr);
        System.out.println("Tipe data double dalam bentuk String : " + angkaDesimalStr);
        System.out.println("Tipe data boolean dalam bentuk String : " + benar_SalahStr);
    }
}
